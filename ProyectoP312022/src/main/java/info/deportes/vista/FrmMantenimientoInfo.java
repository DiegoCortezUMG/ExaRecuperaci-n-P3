    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id campeonato:");
        modelo.addColumn("Nombre:");
        modelo.addColumn("Cantidad de equipos:");
        modelo.addColumn("Id sede:");
        
        Daocampeonato campeonatoDAO = new Daocampeonato();
        List<Clscampeonato> campeonatos = campeonatoDAO.select();
        tablaVendedores.setModel(modelo);
        String[] dato = new String[8];
        for (int i = 0; i < campeonatos.size(); i++) {
            dato[0] = Integer.toString(campeonatos.get(i).getIdcampeonato());
            dato[1] = campeonatos.get(i).getNombrecampeonato();
            dato[3] = Integer.toString(campeonatos.get(i).getCantidadequipos());
            dato[4] = Integer.toString(campeonatos.get(i).getSedepoli());
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

    public void buscarVendedor() {
        Clscampeonato campeonatoAConsultar = new Clscampeonato();
        Daocampeonato campeonatoDAO = new Daocampeonato();
        campeonatoAConsultar.setIdcampeonato(Integer.parseInt(txtbuscado.getText()));
        campeonatoAConsultar = campeonatoDAO.query(campeonatoAConsultar);
        txtNombre.setText(campeonatoAConsultar.getNombrecampeonato());        
    }

